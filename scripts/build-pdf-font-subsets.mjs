#!/usr/bin/env node
/**
 * Build subsetted Noto fonts for the clinical PDF export's ZH and AR locales.
 *
 * Input: Noto Sans SC + Noto Sans Arabic WOFF2 files from @fontsource packages.
 *        (The @fontsource packages no longer ship raw TTF — only WOFF / WOFF2.
 *        subset-font's harfbuzzjs core accepts SFNT, WOFF, or WOFF2 input via
 *        fontverter and converts to any target format. We target 'sfnt' so the
 *        output is TrueType, which jsPDF's addFileToVFS + addFont expect.)
 *
 * Output: src/lib/exportPdf/fonts/zh.ts + ar.ts with base64-encoded subsetted TTFs
 *         and a register-function appended to each module.
 *
 * Re-run this whenever the PDF strings table gains new characters in ZH or AR.
 * Run: node scripts/build-pdf-font-subsets.mjs
 *
 * Idempotent: the script extracts the locale string coverage from strings.ts
 * (the source of truth) and re-emits the modules. Re-running produces byte-
 * identical output as long as the input fonts + strings haven't changed.
 *
 * SIL OFL 1.1 license note: the Noto Sans fonts are licensed under SIL OFL
 * 1.1 — redistribution of subsetted derivatives is permitted as long as the
 * RFN ('Reserved Font Name') restrictions are respected. We name the jsPDF
 * font 'NotoSansSC' / 'NotoSansArabic' (matching the official family name)
 * because the subset is functionally a re-encoding of the same glyph set,
 * not a derivative work that adds new glyphs or alters existing ones.
 */
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import subsetFont from 'subset-font';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');

// Common glyphs that must be in every subset regardless of strings content.
// Digits, punctuation, whitespace cover (a) volume/percentage numerals,
// (b) clinical labels like '24HV', '4.0', '06:00', (c) separators like ', / : .'
// inside dynamic strings (e.g. footer "Page n / total").
const COMMON =
  '0123456789' +
  // Latin alphabet — needed because brand 'My Flow Check' + abbreviations
  // 'FMV' / 'DV' / 'MVV' / 'IPC' stay Latin in ZH and AR strings.
  'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz' +
  // Punctuation, separators, accents that may appear in numeric formatting or
  // mixed-script labels.
  ' \n\t.,;:!?\'"()[]{}-—–_/\\|&%$#@*+=<>°·•' +
  // ASCII punctuation that date-fns may emit in ZH or AR formatted dates.
  '·';

/**
 * Read the strings.ts source file and extract every string literal inside the
 * PDF_STRINGS[locale] block. This is the source of truth for which glyphs the
 * subset must cover. We parse as text (not via TS import) to keep the script
 * runnable as a plain Node ES module without a TS loader.
 */
async function loadStringsCoverage() {
  const txt = await readFile(resolve(root, 'src/lib/exportPdf/strings.ts'), 'utf8');
  const extractBlock = (locale) => {
    const marker = `  ${locale}: {`;
    const start = txt.indexOf(marker);
    if (start < 0) return '';
    let depth = 0;
    let end = start;
    for (let i = start; i < txt.length; i++) {
      if (txt[i] === '{') depth++;
      else if (txt[i] === '}') {
        depth--;
        if (depth === 0) {
          end = i + 1;
          break;
        }
      }
    }
    return txt.slice(start, end);
  };
  const extractStrings = (block) => {
    // Match single-quoted, double-quoted, and template-literal strings.
    // Captures the inner contents, ignoring the quote characters themselves.
    const re = /(?:'([^'\\]*(?:\\.[^'\\]*)*)'|"([^"\\]*(?:\\.[^"\\]*)*)"|`([^`]*?)`)/g;
    let out = '';
    let m;
    while ((m = re.exec(block)) !== null) {
      out += m[1] || m[2] || m[3] || '';
    }
    return out;
  };
  return {
    zh: extractStrings(extractBlock('zh')),
    ar: extractStrings(extractBlock('ar')),
  };
}

/**
 * Subset one font file (regular or bold) to the given glyph coverage and
 * return a base64-encoded SFNT (TrueType) buffer.
 */
async function subsetToBase64(sourcePath, glyphs) {
  const buf = await readFile(sourcePath);
  const out = await subsetFont(buf, glyphs, { targetFormat: 'sfnt' });
  return { base64: out.toString('base64'), bytes: out.length };
}

function moduleSource({
  bannerLines,
  regularConstName,
  regularBase64,
  boldConstName,
  boldBase64,
  jspdfName,
  jspdfNameConst,
  registerFn,
  registerFnBody,
}) {
  return (
    `// AUTO-GENERATED by scripts/build-pdf-font-subsets.mjs. Do NOT hand-edit.\n` +
    bannerLines.map((l) => `// ${l}`).join('\n') +
    `\n\nimport type { jsPDF } from 'jspdf';\n\n` +
    `export const ${regularConstName} = '${regularBase64}';\n` +
    `export const ${boldConstName} = '${boldBase64}';\n` +
    `export const ${jspdfNameConst} = '${jspdfName}';\n\n` +
    `export function ${registerFn}(doc: jsPDF): void {\n` +
    registerFnBody +
    `}\n`
  );
}

async function buildLocale(locale, glyphs, sources, jspdfName, vfsPrefix, varPrefix, registerFn, extraBody = '') {
  const regOut = await subsetToBase64(sources.regular, glyphs);
  const boldOut = await subsetToBase64(sources.bold, glyphs);
  const uniqueGlyphCount = new Set(glyphs).size;
  const banner = [
    `Source files: ${sources.regular.replace(root + '/', '')}, ${sources.bold.replace(root + '/', '')}`,
    `Glyph coverage: ${uniqueGlyphCount} unique chars (extracted from src/lib/exportPdf/strings.ts ${locale} block + COMMON range)`,
    `Subset sizes: regular=${(regOut.bytes / 1024).toFixed(1)} KB, bold=${(boldOut.bytes / 1024).toFixed(1)} KB`,
    `Generated at: ${new Date().toISOString().slice(0, 10)} (UTC date only — re-run for fresh embedding)`,
  ];
  const registerFnBody =
    `  doc.addFileToVFS('${jspdfName}-Regular.ttf', ${varPrefix}_REGULAR_BASE64);\n` +
    `  doc.addFont('${jspdfName}-Regular.ttf', ${varPrefix}_JSPDF_NAME, 'normal');\n` +
    `  doc.addFileToVFS('${jspdfName}-Bold.ttf', ${varPrefix}_BOLD_BASE64);\n` +
    `  doc.addFont('${jspdfName}-Bold.ttf', ${varPrefix}_JSPDF_NAME, 'bold');\n` +
    extraBody;
  const src = moduleSource({
    bannerLines: banner,
    regularConstName: `${varPrefix}_REGULAR_BASE64`,
    regularBase64: regOut.base64,
    boldConstName: `${varPrefix}_BOLD_BASE64`,
    boldBase64: boldOut.base64,
    jspdfName,
    jspdfNameConst: `${varPrefix}_JSPDF_NAME`,
    registerFn,
    registerFnBody,
  });
  return { src, regBytes: regOut.bytes, boldBytes: boldOut.bytes };
}

async function main() {
  const coverage = await loadStringsCoverage();
  if (!coverage.zh || !coverage.ar) {
    console.error('ERROR: could not extract ZH or AR strings from src/lib/exportPdf/strings.ts.');
    console.error('       Has the strings table been updated with both locales? (Task 3 of plan 09-05.)');
    process.exit(1);
  }

  // Deduplicate via Set + join. Order doesn't matter for harfbuzz.
  const zhGlyphs = [...new Set(COMMON + coverage.zh)].join('');
  const arGlyphs = [...new Set(COMMON + coverage.ar)].join('');

  const SC_SOURCES = {
    regular: resolve(root, 'node_modules/@fontsource/noto-sans-sc/files/noto-sans-sc-chinese-simplified-400-normal.woff2'),
    bold: resolve(root, 'node_modules/@fontsource/noto-sans-sc/files/noto-sans-sc-chinese-simplified-700-normal.woff2'),
  };
  const AR_SOURCES = {
    regular: resolve(root, 'node_modules/@fontsource/noto-sans-arabic/files/noto-sans-arabic-arabic-400-normal.woff2'),
    bold: resolve(root, 'node_modules/@fontsource/noto-sans-arabic/files/noto-sans-arabic-arabic-700-normal.woff2'),
  };

  const fontsDir = resolve(root, 'src/lib/exportPdf/fonts');
  await mkdir(fontsDir, { recursive: true });

  // ZH (Simplified Chinese, LTR)
  console.log(`[zh] subsetting Noto Sans SC to ${new Set(zhGlyphs).size} glyphs...`);
  const zh = await buildLocale(
    'zh',
    zhGlyphs,
    SC_SOURCES,
    'NotoSansSC',
    'NotoSansSC',
    'NOTO_SC',
    'registerZhFont',
  );
  const zhPath = resolve(fontsDir, 'zh.ts');
  await writeFile(zhPath, zh.src, 'utf8');
  console.log(`[zh] wrote ${zhPath.replace(root + '/', '')} (regular ${(zh.regBytes / 1024).toFixed(1)} KB, bold ${(zh.boldBytes / 1024).toFixed(1)} KB)`);

  // AR (Modern Standard Arabic, RTL — adds doc.setR2L(true))
  console.log(`[ar] subsetting Noto Sans Arabic to ${new Set(arGlyphs).size} glyphs...`);
  const ar = await buildLocale(
    'ar',
    arGlyphs,
    AR_SOURCES,
    'NotoSansArabic',
    'NotoSansArabic',
    'NOTO_AR',
    'registerArFont',
    // AR is RTL: jsPDF's setR2L(true) reorders text emission for the rest of
    // the document. Called only when the AR font is registered, never per-page.
    `  // AR is RTL — flip text direction for the rest of the document.\n` +
      `  doc.setR2L(true);\n`,
  );
  const arPath = resolve(fontsDir, 'ar.ts');
  await writeFile(arPath, ar.src, 'utf8');
  console.log(`[ar] wrote ${arPath.replace(root + '/', '')} (regular ${(ar.regBytes / 1024).toFixed(1)} KB, bold ${(ar.boldBytes / 1024).toFixed(1)} KB)`);

  const totalKb = ((zh.regBytes + zh.boldBytes + ar.regBytes + ar.boldBytes) / 1024).toFixed(1);
  console.log(`\nTotal subset size: ${totalKb} KB (raw TTF; base64 encoding inflates ~33% inside the .ts modules)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
